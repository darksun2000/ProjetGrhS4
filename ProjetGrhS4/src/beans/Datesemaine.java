package beans;

public class Datesemaine {

	private int idDateSemaine;
	private String dateVendredi;
	private int auto;
	public int getIdDateSemaine() {
		return idDateSemaine;
	}
	public void setIdDateSemaine(int idDateSemaine) {
		this.idDateSemaine = idDateSemaine;
	}
	public String getDateVendredi() {
		return dateVendredi;
	}
	public void setDateVendredi(String dateVendredi) {
		this.dateVendredi = dateVendredi;
	}
	public int getAuto() {
		return auto;
	}
	public void setAuto(int auto) {
		this.auto = auto;
	}
}
